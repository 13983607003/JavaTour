package com.zrkc.syyh.model;

public class InspectionWorker {
	private long workerId;
	private long groupId;
	private String workerName;
	private String workerTel1;
	private String workerTel2;
	public long getWorkerId() {
		return workerId;
	}
	public void setWorkerId(long workerId) {
		this.workerId = workerId;
	}
	public long getGroupId() {
		return groupId;
	}
	public void setGroupId(long groupId) {
		this.groupId = groupId;
	}
	public String getWorkerName() {
		return workerName;
	}
	public void setWorkerName(String workerName) {
		this.workerName = workerName;
	}
	public String getWorkerTel1() {
		return workerTel1;
	}
	public void setWorkerTel1(String workerTel1) {
		this.workerTel1 = workerTel1;
	}
	public String getWorkerTel2() {
		return workerTel2;
	}
	public void setWorkerTel2(String workerTel2) {
		this.workerTel2 = workerTel2;
	}
	public InspectionWorker() {
		super();
		// TODO Auto-generated constructor stub
	}
	public InspectionWorker(long workerId, long groupId, String workerName,
			String workerTel1, String workerTel2) {
		super();
		this.workerId = workerId;
		this.groupId = groupId;
		this.workerName = workerName;
		this.workerTel1 = workerTel1;
		this.workerTel2 = workerTel2;
	}
	@Override
	public String toString() {
		return "PiWorker [groupId=" + groupId + ", workerId=" + workerId
				+ ", workerName=" + workerName + ", workerTel1=" + workerTel1
				+ ", workerTel2=" + workerTel2 + "]";
	}
	
}
